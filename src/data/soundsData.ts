export const generateCV = (sound: string): SyllableItem[] => {
  const s = sound.toLowerCase();
  return vowels.map(v => ({
    syllable: `${s}${v}`,
    display: `${s}${v}`,
  }));
};

export const generateCVCV = (sound: string): SyllableItem[] => {
  const s = sound.toLowerCase();
  return vowels.map(v => ({
    syllable: `${s}${v}${s}${v}`,
    display: `${s}${v}${s}${v}`,
  }));
};

