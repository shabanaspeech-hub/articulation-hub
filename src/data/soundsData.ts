export const generateVC = (sound: string): SyllableItem[] => {
  const s = sound.toUpperCase();
  if (vcTargets[s]) return vcTargets[s];
  // Fallback for other sounds
  const sl = sound.toLowerCase();
  return vowels.map(v => ({
    syllable: `${v}${sl}`,
    display: `${v}${sl}`,
  }));
};

